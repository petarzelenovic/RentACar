    public void popuniTabelu() {
        try {
            ArrayList<Car> cars = ClientController.getInstance().getAllCar();
            for (Car car : cars) {
                System.out.println(car.getHorsePower());
            }
            TableModelAutomobil mt = (TableModelAutomobil) jTable1.getModel();
            mt.ubaciListu(cars);
        } catch (Exception ex) {
            Logger.getLogger(ObrisiAutomobilForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

