    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            int row = jTable1.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Izaberite red");
                return;
            }
            TableModelAutomobil mt = (TableModelAutomobil) jTable1.getModel();

            Car car = mt.vratiAutomobil(row);
            ClientController.getInstance().deleteCar(car);
            JOptionPane.showMessageDialog(this, "Uspesno obrisan automobil");
            popuniTabelu();

        } catch (Exception ex) {
            if (ex instanceof java.sql.SQLIntegrityConstraintViolationException) {
                JOptionPane.showMessageDialog(this, "Ovaj automobil je rentiran");
                return;
            }
//            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

