    private void jButtonIzmeniRentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniRentActionPerformed
        // TODO add your handling code here:
        TableModelPotvrda mt = (TableModelPotvrda) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Odaberite red!");
            return;
        }
        Confirmation potvrda = mt.vratiPotvrdu(row);
        IzmenaPotvrdeForm forma = new IzmenaPotvrdeForm(this, true, potvrda);
        System.out.println("POTVRDA ID:" + potvrda.getConfirmationID());
        forma.setVisible(true);
        popuniTabelu();
    }//GEN-LAST:event_jButtonIzmeniRentActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumOd = null;
            if (!jFormattedText.getText().isEmpty()) {
                datumOd = sdf.parse(jFormattedText.getText());
            }
            String registracija = jFormattedReg.getText();
            TableModelPotvrda mt = (TableModelPotvrda) jTable1.getModel();
            ArrayList<Confirmation> listaFiltriranih = mt.vratiListuFiltriranih(datumOd, registracija);
            mt.ubaciListu(listaFiltriranih);
        } catch (ParseException ex) {
            Logger.getLogger(PotvrdeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

