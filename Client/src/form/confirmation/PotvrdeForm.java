    public void popuniTabelu() {
        try {
            TableModelPotvrda mt = (TableModelPotvrda) jTable1.getModel();
            ArrayList<Confirmation> potvrde = ClientController.getInstance().getAllConfirmation();
            mt.ubaciListu(potvrde);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(PotvrdeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
